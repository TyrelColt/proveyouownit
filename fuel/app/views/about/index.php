<h2>What is ProveYouOwnIt.com?</h2>
  <p>
      ProveYouOwnIt.com is a place for you to keep records of all the items you can't live without.  
      In the event of a lost or stolen item you can use ProveYouOwnIt to make insurance claims, or
      in some cases, even have your items returned to you.
  </p>
  <p>
    The ProveYouOwnIt database can be searched by others who may have found a lost or stolen item to
    check if it has been reported as missing by a member.  This will all be done without giving away
    any information about the person who has the missing item.  The person with a missing item will
    be contacted directly on the ProveYouOwnIt website.
  </p>
  <hr />
  <br />
  <h2>What Do You Need to ProveYouOwnIt</h2>
  <p>
    There are a series of credentials that ProveYouOwnIt looks for to verify that you are indeed the
    owner of an item.  The more of these that you provide, the higher your 'Own-It-Score' will be.
    ProveYouOwnIt.com will verify ownership with any item that reaches an Own-It-Score of 4 or higher.
  </p>
  <p>
    <h3>Verification Steps</h3>
    <ul>
      <li>Picture of Item (+1)</li>
      <li>Serial Number (+1)</li>
      <li>Store receipt (+1)</li>
      <li>Store Verification (+5)</li>
    </ul>
  </p>